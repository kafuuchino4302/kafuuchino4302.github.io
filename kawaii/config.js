export const getRatingLabel = (rating) => {
    if (rating <= 20) return '还在长大中～';
    if (rating <= 40) return '有点可爱啦';
    if (rating <= 60) return '萌萌哒呢';
    if (rating <= 80) return '超级卡哇伊～';
    return '爆表啦ฅ^•ﻌ•^ฅ';
  
};
