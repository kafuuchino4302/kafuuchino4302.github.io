export const getRatingLabel = (score) => {
  if (score < 20) return '有点酷酷的 🧊';
  if (score < 40) return '略带呆萌 🐢';
  if (score < 60) return '普通可爱 🐰';
  if (score < 80) return '很有魅力 ✨';
  return '萌化我心 💖';
};